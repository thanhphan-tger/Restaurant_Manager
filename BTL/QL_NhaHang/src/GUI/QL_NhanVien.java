    private void loadCBOChucVu(String ten)
    {
        cboCVu.removeAllItems();
        int index = 0;
        try {
            ResultSet rs = exec.selectData("SELECT * FROM CHUCVU");
            while (rs.next())
            {
                String maCV = rs.getString("MACV");
                String tenCV = rs.getString("TENCV");
                cboCVu.addItem(new ChucVu(maCV, tenCV));
                if(tenCV.equals(ten))
                    cboCVu.setSelectedIndex(index);
                index++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(QL_NhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

