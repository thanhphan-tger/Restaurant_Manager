    public void loadData(){
        tblModel.setRowCount(0);
        try {
            ResultSet resultSet =exec.selectData("exec sp_SelectNV");
            while(resultSet.next())
            {
                tblModel.addRow(new String[]{resultSet.getString(1),resultSet.getString(2),resultSet.getString(3),resultSet.getString(4)});
            }
        } catch (SQLException ex) {
            Logger.getLogger(QL_NhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
