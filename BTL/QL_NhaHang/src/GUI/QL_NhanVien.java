    private void btnXoaNhanVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaNhanVienActionPerformed
        // TODO add your handling code here:
        int[] rows=tblDSNhanVien.getSelectedRows();
        if(rows.length==0)
            JOptionPane.showMessageDialog(this,"Vui lòng chọn ít nhất 1 nhân viên để xóa thông tin!", "Lỗi lựa chọn!",HEIGHT);
        else
        {
            int total=0;
            int reply = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa "+rows.length+" nhân viên vừa chọn không ?","Xác nhận !", JOptionPane.YES_NO_OPTION);
            if(reply==JOptionPane.YES_OPTION)
            {
                for (int i = 0; i < rows.length; i++) {
                if(dao.deleteNV((String)tblDSNhanVien.getValueAt(rows[i], 0))==1)
                    total++;
                }
                if(total==0)
                    JOptionPane.showMessageDialog(this,"Xóa không thành công!", "Không thành công!",JOptionPane.INFORMATION_MESSAGE);
                else
                {
                    JOptionPane.showMessageDialog(this,"Xóa thành công "+total+" nhân viên!", "Thành công!",JOptionPane.INFORMATION_MESSAGE);
                    refreshForm();
                }
            }
            else
                refreshForm();
        }
    }//GEN-LAST:event_btnXoaNhanVienActionPerformed

