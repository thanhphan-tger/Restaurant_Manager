    private void btnXoaKhachHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaKhachHangActionPerformed
        // TODO add your handling code here:
        int[] rows=tblDSKhachHang.getSelectedRows();
        if(rows.length==0)
            JOptionPane.showMessageDialog(this,"Vui lòng chọn ít nhất 1 khách hàng để xóa thông tin!", "Lỗi lựa chọn!",HEIGHT);
        else
        {
            int total=0;
            int reply = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa "+rows.length+" khách hàng vừa chọn không ?","Xác nhận !", JOptionPane.YES_NO_OPTION);
            if(reply==JOptionPane.YES_OPTION)
            {
                for (int i = 0; i < rows.length; i++) {
                if(dao.deleteKH((String)tblDSKhachHang.getValueAt(rows[i], 0))==1)
                    total++;
                }
                if(total==0)
                    JOptionPane.showMessageDialog(this,"Xóa không thành công!", "Không thành công!",JOptionPane.INFORMATION_MESSAGE);
                else
                {
                    JOptionPane.showMessageDialog(this,"Xóa thành công "+total+" khách hàng!", "Thành công!",JOptionPane.INFORMATION_MESSAGE);
                    refreshForm();
                }
            }
            else
                refreshForm();
        }
    }//GEN-LAST:event_btnXoaKhachHangActionPerformed

    private void btnSuaKhachHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaKhachHangActionPerformed
        // TODO add your handling code here:
        KhachHang khachHang=new KhachHang(txtMaKhachHang.getText(),txtTenKhachHang.getText(),txtSDTKhachHang.getText());
        boolean validate_ID=dao.validate_ID(khachHang.getMaKH());
        boolean validate_Number=dao.validate_PhoneNumber(khachHang.getSDT(), khachHang.getMaKH());
        if(txtMaKhachHang.getText().isEmpty() ||txtTenKhachHang.getText().isEmpty() ||txtSDTKhachHang.getText().isEmpty())
            JOptionPane.showMessageDialog(this,"Vui lòng nhập đầy đủ thông tin khách hàng!", "Lỗi nhập liệu!",HEIGHT);
        else if(txtSDTKhachHang.getText().length()<10)
            JOptionPane.showMessageDialog(this,"Vui lòng nhập đúng số điện thoại (10-11 số)!", "Lỗi nhập liệu!",HEIGHT);
        else if(validate_ID==true)
        {
             JOptionPane.showMessageDialog(this,"Mã khách hàng không tồn tại!", "Lỗi nhập liệu!",HEIGHT);
        }
        else if(validate_Number==false)
            JOptionPane.showMessageDialog(this,"Số điện thoại đã tồn tại!", "Lỗi nhập liệu!",HEIGHT);
        else
        {
           dao.updateKH(khachHang);
            refreshForm();
        }
            
    }//GEN-LAST:event_btnSuaKhachHangActionPerformed

