    public void loadData(){
        tblModel.setRowCount(0);
        try {
            ResultSet resultSet =exec.selectData("exec sp_SelectKH");
            while(resultSet.next())
            {
                tblModel.addRow(new String[]{resultSet.getString(1),resultSet.getString(2),resultSet.getString(3)});
            }
        } catch (SQLException ex) {
            Logger.getLogger(QL_KhachHang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public Integer updateSearchedData()
    {
        tblModel.setRowCount(0);
        int count=0;
        try {
            ResultSet resultSet =dao.findKH(txtTimKiem.getText());
            
            while(resultSet.next())
            {
                tblModel.addRow(new String[]{resultSet.getString(1),resultSet.getString(2),resultSet.getString(3)});
                count++;
            }
            return count;
        } catch (SQLException ex) {
            Logger.getLogger(QL_KhachHang.class.getName()).log(Level.SEVERE, null, ex);
        }
        return count;
    }
