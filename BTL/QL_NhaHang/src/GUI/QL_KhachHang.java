    private void btnThemKhachHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemKhachHangActionPerformed
        // TODO add your handling code here:
        KhachHang khachHang=new KhachHang(txtMaKhachHang.getText(),txtTenKhachHang.getText(),txtSDTKhachHang.getText());
        boolean validate_id=dao.validate_ID(khachHang.getMaKH());
        boolean validate_number=dao.validate_PhoneNumber(khachHang.getSDT(),khachHang.getMaKH());
        if(txtMaKhachHang.getText().isEmpty() ||txtTenKhachHang.getText().isEmpty() ||txtSDTKhachHang.getText().isEmpty())
            JOptionPane.showMessageDialog(this,"Vui lòng nhập đầy đủ thông tin khách hàng!", "Lỗi nhập liệu!",HEIGHT);
        else if(txtSDTKhachHang.getText().length()<10)
            JOptionPane.showMessageDialog(this,"Vui lòng nhập đúng số điện thoại (10-11 số)!", "Lỗi nhập liệu!",HEIGHT);
        else if(validate_number==false)
            JOptionPane.showMessageDialog(this,"Số điện thoại đã tồn tại!", "Lỗi nhập liệu!",HEIGHT);
        else if(validate_id==false)
        {
            int reply = JOptionPane.showConfirmDialog(this, "Mã khách hàng đã tồn tại. Bạn có muốn thay đổi thông tin của khách hàng không?","Xác nhận thay đổi thông tin!", JOptionPane.YES_NO_OPTION);
            if(reply==JOptionPane.YES_OPTION)
            {
                dao.updateKH(khachHang);
                refreshForm();
            }
            else
                refreshForm();
        }
        else
        {
            dao.insertKH(khachHang);
            refreshForm();
        }
    }//GEN-LAST:event_btnThemKhachHangActionPerformed

    
